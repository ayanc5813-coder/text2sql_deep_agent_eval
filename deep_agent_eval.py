# =========================================================

def run_deep_agent(question):

    state = init_state(question)

    state = start_timer(state)

    # ----------------------
    # planner
    # ----------------------

    state = planner_agent(
        state
    )

    # ----------------------
    # schema explorer
    # ----------------------

    state = schema_explorer_agent(
        state
    )

    # ----------------------
    # sql generation
    # ----------------------

    state = sql_generator_agent(
        state
    )

    # ----------------------
    # validation
    # ----------------------

    state = validate_sql(
        state
    )

    # ----------------------
    # execute
    # ----------------------

    state = execute_sql_query(
        state
    )

    # ----------------------
    # repair loop
    # ----------------------

    while should_repair(state):

        state = repair_sql_agent(
            state
        )

        state = execute_sql_query(
            state
        )

    # ----------------------
    # final answer
    # ----------------------

    if state["execution_error"] == "":

        state = answer_generator_agent(
            state
        )

    else:

        state["answer"] = (

            "Agent failed after "
            f"{state['repair_attempts']} "
            "repair attempts."
        )

    state = stop_timer(state)

    return state


