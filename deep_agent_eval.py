class DatabaseManager:

    def __init__(self):

        self.conn = sqlite3.connect(
            Settings.DB_FILE
        )

    # --------------------------------------
    # list tables
    # --------------------------------------

    def get_tables(self):

        cursor = self.conn.cursor()

        cursor.execute("""

        SELECT name
        FROM sqlite_master
        WHERE type='table'

        """)

        return [

            x[0]

            for x in cursor.fetchall()

        ]

    # --------------------------------------
    # table schema
    # --------------------------------------

    def get_table_schema(

        self,

        table_name

    ):

        cursor = self.conn.cursor()

        cursor.execute(

            f"PRAGMA table_info({table_name})"

        )

        return cursor.fetchall()

    # --------------------------------------
    # execute sql
    # --------------------------------------

    def execute(

        self,

        sql_query

    ):

        cursor = self.conn.cursor()

        cursor.execute(
            sql_query
        )

        rows = cursor.fetchall()

        return rows

# =========================================================
# SCHEMA EXTRACTION
# =========================================================

def build_schema_text(

    db: DatabaseManager

):

    schema_parts = []

    tables = db.get_tables()

    for table in tables:

        schema_parts.append(
            f"\nTABLE: {table}"
        )

        columns = db.get_table_schema(
            table
        )

        for col in columns:

            column_name = col[1]

            column_type = col[2]

            schema_parts.append(

                f"{column_name} "
                f"({column_type})"

            )

    return "\n".join(
        schema_parts
    )

