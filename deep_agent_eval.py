def invoke_llm(
    prompt: str,
    retries: int = 5,
    backoff: int = 3
):

    last_error = None

    for attempt in range(retries):

        try:

            response = llm.invoke(prompt)

            content = response.content

            if isinstance(content, list):

                content = "\n".join(
                    str(x)
                    for x in content
                )

            return str(content).strip()

        except Exception as e:

            last_error = e

            print(
                f"[Retry {attempt+1}/{retries}] {e}"
            )

            time.sleep(
                backoff * (attempt + 1)
            )

    raise RuntimeError(
        f"LLM call failed:\n{last_error}"
    )

