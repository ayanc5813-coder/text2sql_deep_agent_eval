def exact_match_evaluator(pred, gold):

    if not gold:
        return 0

    return int(
        str(pred).strip().lower()
        ==
        str(gold).strip().lower()
    )


def sql_presence_score(sql):

    if not sql:
        return 0

    keywords = ["SELECT", "FROM"]

    return int(
        all(k in sql.upper() for k in keywords)
    )


def execution_success_score(error):

    return 1 if not error else 0

# =========================================================
# LLM JUDGE (optional but powerful)
# =========================================================

def llm_judge(question, answer):

    prompt = f"""
You are an evaluator.

Question:
{question}

Answer:
{answer}

Score from 0 to 10 based on correctness only.

Return only a number.
"""

    try:

        resp = judge_llm.invoke(prompt).content

        digits = "".join(
            c for c in resp if c.isdigit()
        )

        return float(digits or 0)

    except:

        return 0

# =========================================================
# COMPOSITE SCORE
# =========================================================

def compute_composite(state, gold_answer=None):

    sql_score = sql_presence_score(
        state.get("sql_query")
    )

    exec_score = execution_success_score(
        state.get("execution_error")
    )

    llm_score = llm_judge(
        state["question"],
        state.get("answer", "")
    )

    exact = exact_match_evaluator(
        state.get("answer"),
        gold_answer
    )

    composite = (
        0.3 * sql_score +
        0.2 * exec_score +
        0.3 * (llm_score / 10) +
        0.2 * exact
    )

    return {
        "sql_score": sql_score,
        "exec_score": exec_score,
        "llm_score": llm_score,
        "exact_match": exact,
        "composite": round(composite, 3)
    }

# =========================================================
# RUN SINGLE EVAL
# =========================================================

def evaluate_single(question, gold_answer=None):

    state = run_graph_agent(question)

    metrics = compute_composite(
        state,
        gold_answer
    )

    return {
        "question": question,
        "answer": state["answer"],
        "sql": state["sql_query"],
        "error": state["execution_error"],
        "latency": state["latency"],
        "trajectory_steps": len(state["trajectory"]),
        **metrics
    }

